package datatypesandveriables;

public class arthematicclass {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		
		//+ operator
		System.out.println(a+b);
		
		//- operator 
		System.out.print(b-a);
		
		// * operator 
		System.out.print(a*b);
		
		// % - mode
		System.out.print(17%19);
		

	}

}
